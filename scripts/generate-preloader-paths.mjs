/**
 * Regenerate normalized paths from Figma exports in public/images/preloader/.
 * Run: node scripts/generate-preloader-paths.mjs
 */
import { readFileSync, writeFileSync } from 'node:fs'
import { createRequire } from 'node:module'
import SVGPathCommander from 'svg-path-commander'

const require = createRequire(import.meta.url)
const root = new URL('..', import.meta.url).pathname

const TARGET = 24
const PADDING = 0.12
const COLLAPSED = 'M12 12 L12.001 12'
const INNER_SLOTS = 4

const defs = {
  hardware: {
    file: 'public/images/preloader/Hardware.svg',
    label: 'Hardware',
    frameIndex: 3,
    innerIndices: [0, 1, 2],
  },
  mobile: {
    file: 'public/images/preloader/Mobile.svg',
    label: 'Mobile',
    frameIndex: 2,
    innerIndices: [1, 0],
  },
  webApps: {
    file: 'public/images/preloader/WebApp.svg',
    label: 'Web Apps',
    frameIndex: 1,
    innerIndices: [0],
    circleIndices: [0, 1, 2],
  },
  everything: {
    file: 'public/images/preloader/Between.svg',
    label: 'Everything In Between',
    frameIndex: 3,
    innerIndices: [1, 2, 0],
  },
}

function normalizePath(d, vw, vh) {
  const inner = TARGET * (1 - PADDING * 2)
  const scale = Math.min(inner / vw, inner / vh)
  const tx = (TARGET - vw * scale) / 2
  const ty = (TARGET - vh * scale) / 2
  return new SVGPathCommander(d).transform({ scale, translate: [tx, ty] }).toString()
}

function circleToPath(cx, cy, r) {
  return `M ${cx - r} ${cy} a ${r} ${r} 0 1 0 ${r * 2} 0 a ${r} ${r} 0 1 0 ${-r * 2} 0`
}

function extractPaths(svg) {
  const paths = [...svg.matchAll(/<path d="([^"]+)"/g)].map((m) => m[1])
  const circles = [...svg.matchAll(/<circle cx="([^"]+)" cy="([^"]+)" r="([^"]+)"/g)].map(
    (m) => circleToPath(+m[1], +m[2], +m[3]),
  )
  return { paths, circles }
}

function viewBox(svg) {
  const m = svg.match(/viewBox="0 0 ([^ ]+) ([^"]+)"/)
  return [+m[1], +m[2]]
}

const icons = Object.entries(defs).map(([id, def]) => {
  const svg = readFileSync(`${root}/${def.file}`, 'utf8')
  const [vw, vh] = viewBox(svg)
  const { paths, circles } = extractPaths(svg)
  const frame = normalizePath(paths[def.frameIndex], vw, vh)
  const innersRaw = [
    ...def.innerIndices.map((i) => paths[i]),
    ...(def.circleIndices ? def.circleIndices.map((i) => circles[i]) : []),
  ].map((d) => normalizePath(d, vw, vh))
  while (innersRaw.length < INNER_SLOTS) innersRaw.push(COLLAPSED)
  return { id, label: def.label, frame, inners: innersRaw.slice(0, INNER_SLOTS) }
})

const { interpolate } = require('flubber')
for (let i = 0; i < icons.length; i++) {
  const a = icons[i]
  const b = icons[(i + 1) % icons.length]
  interpolate(a.frame, b.frame)(0.5)
  for (let j = 0; j < INNER_SLOTS; j++) {
    interpolate(a.inners[j], b.inners[j])(0.5)
  }
}

const lines = [
  `/** Generated by scripts/generate-preloader-paths.mjs — do not edit by hand. */`,
  ``,
  `export const PRELOADER_VIEWBOX = ${TARGET}`,
  `export const INNER_SLOT_COUNT = ${INNER_SLOTS}`,
  `export const COLLAPSED_INNER_PATH = '${COLLAPSED}'`,
  ``,
  `export type PreloaderIconId = ${icons.map((i) => `'${i.id}'`).join(' | ')}`,
  ``,
  `export type PreloaderIconDef = {`,
  `  id: PreloaderIconId`,
  `  label: string`,
  `  frame: string`,
  `  inners: readonly string[]`,
  `}`,
  ``,
  `export const PRELOADER_ICONS: readonly PreloaderIconDef[] = [`,
  ...icons.flatMap((icon) => [
    `  {`,
    `    id: '${icon.id}',`,
    `    label: '${icon.label}',`,
    `    frame:`,
    `      '${icon.frame}',`,
    `    inners: [`,
    ...icon.inners.map((p) => `      '${p}',`),
    `    ],`,
    `  },`,
  ]),
  `] as const`,
  ``,
  `export const PRELOADER_ICON_COUNT = PRELOADER_ICONS.length`,
  `export const PRELOADER_HARDWARE_FRAME = PRELOADER_ICONS[0].frame`,
  `export const PRELOADER_HARDWARE_INNERS = PRELOADER_ICONS[0].inners`,
]

writeFileSync(`${root}/lib/preloader/iconPaths.ts`, lines.join('\n') + '\n')
console.log('Wrote lib/preloader/iconPaths.ts')
